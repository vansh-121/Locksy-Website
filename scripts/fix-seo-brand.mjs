import { readdirSync, readFileSync, writeFileSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const POSTS_DIR = join(__dirname, '..', 'lib', 'posts')

const REPLACEMENTS = [
    [/\ba tab-locking extension-protected\b/gi, 'Locksy-protected'],
    [/\ba tab-locking extension's\b/gi, "Locksy's"],
    [/\ba tab-locking extension\b/gi, 'Locksy'],
    [/\ba tab locking extension\b/gi, 'Locksy'],
    [/\btab-locking extension's\b/gi, "Locksy's"],
    [/\btab-locking extension\b/gi, 'Locksy'],
    [/\btab-locking extensions\b/gi, 'Locksy extensions'],
    [/\bnicoryne\/a tab-locking extension\b/gi, 'vansh-121/Locksy'], // Fix wrong repository URLs if any
]

let totalFilesFixed = 0

// 1. Fix Individual Posts
const individualFiles = readdirSync(POSTS_DIR)
    .filter(f => f.endsWith('.ts'))

for (const file of individualFiles) {
    const filePath = join(POSTS_DIR, file)
    let content = readFileSync(filePath, 'utf-8')
    let originalContent = content

    for (const [pattern, replacement] of REPLACEMENTS) {
        content = content.replace(pattern, replacement)
    }

    // Clean up any double "a" generated by grammatical quirks, e.g. "while a a tab-locking..."
    content = content.replace(/\ba a Locksy-protected\b/gi, 'a Locksy-protected')
    content = content.replace(/\ba a Locksy\b/gi, 'a Locksy')
    content = content.replace(/\bthe a Locksy\b/gi, 'the Locksy')

    if (content !== originalContent) {
        writeFileSync(filePath, content, 'utf-8')
        totalFilesFixed++
        console.log(`✅ Fixed brand name in: ${file}`)
    }
}

console.log(`🎉 Finished! Brand names updated in ${totalFilesFixed} files.`)
